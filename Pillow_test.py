def matrice_en_image(matrice, taille_pixel=20):
    largeur = len(matrice[0]) * taille_pixel
    hauteur = len(matrice) * taille_pixel

    image = Image.new("RGB", (largeur, hauteur))

    couleurs = {
        0: (200, 200, 200),
        1: (0, 0, 0),
        2: (255, 0, 0),
        3: (255, 215, 0),
        4: (138, 43, 226),
    }

    for i, ligne in enumerate(matrice):
        for j, valeur in enumerate(ligne):
            couleur = couleurs.get(valeur, (255, 255, 255))
            for x in range(taille_pixel):
                for y in range(taille_pixel):
                    image.putpixel((j * taille_pixel + x, i * taille_pixel + y), couleur)

    image.show()
    image.save("donjon.png")

